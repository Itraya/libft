#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	int		i;
	char	*t;

	i = 0;
	while (s1[i])
		i++;
	t = malloc(i + 1);
	if (!t)
		return (0);
	i = 0;
	while (s1[i])
	{
		t[i] = s1[i];
		i++;
	}
	t[i] = '\0';
	return (t);
}
