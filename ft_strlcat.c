#include "libft.h"

size_t	ft_strlcat(char *dst, const char *src, size_t dstsize)
{
	unsigned int	i;
	unsigned int	j;
	unsigned int	l;

	i = ft_strlen(dst);
	l = i;
	j = 0;
	if (dstsize < ft_strlen(dst))
		return (dstsize + ft_strlen(src));
	while (src[j] != '\0' && j + l + 1 < dstsize)
	{
		dst[i] = src[j];
		i++;
		j++;
	}
	dst[i] = '\0';
	return (ft_strlen(dst) + ft_strlen(&src[j]));
}
